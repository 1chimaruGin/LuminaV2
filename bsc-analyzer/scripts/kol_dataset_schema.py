def live_csv_columns(*, schema: str = "90d", include_live_extras: bool = False) -> list[str]:
    """
    schema:
      - "90d" — matches kol_dataset_90d.csv (76 cols, concat-friendly with historical CSV)
      - "full" — matches enrich_full_dataset FINAL_COLUMNS (deployer PIT + deployer_csv_*)
    """
    if schema == "90d":
        base = list(DATASET_CSV_COLUMNS_90D)
    elif schema == "full":
        base = list(FINAL_COLUMNS)
    else:
        raise ValueError(f"unknown schema {schema!r}, use '90d' or 'full'")
    if include_live_extras:
        return base + list(LIVE_DATASET_EXTRA_COLUMNS)
    return base
